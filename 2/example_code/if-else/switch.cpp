#include <iostream>
using namespace std;

int main() {
    int type = 3;

    switch (type) {
        case 1:
            cout << "type = 1" << endl;
            break;
        case 2:
            cout << "type = 2" << endl;
            break;
        case 3:
            cout << "type = 3" << endl;
            break;
    }

    return 0;
}